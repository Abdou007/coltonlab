<h1>Overview</h1>
<h2>Spintronics/Quantum Computing</h2>
<p><img border="0" src="images/lab.jpg" width="360" height="202" align="right" alt="Inside our research lab.">
Spin states of electrons in semiconductors have been the focus of much recent 
research due to the twin emerging fields of spintronics and spin-based quantum 
computing. Spintronics involves actively using the spin of an electron together 
with its charge in the operation of nanoscale electronic devices. Interesting 
applications of spintronics include giant magneto-resistance (GMR), the 
discovery of which was awarded the 2007 Nobel Prize in Physics and which now is 
used extensively in hard disk read heads; magnetic random access memory (MRAM) a 
non-volatile memory now being used in applications such as computers in 
satellites; and spin-LEDs, which are light-emitting diodes that produce 
circularly polarized light by orienting the spin of the electrons inside the 
diode structure.</p>
<p>Spin-based quantum computing involves using quantum mechanical spin states as 
the &quot;1&quot;s and &quot;0&quot;s of a computer for storage and calculation. Quantum computers 
will use these states and quantum mechanical operations to solve certain kinds 
of problems exponentially faster than classical computers. Examples of such 
problems include database searching and the cryptographic task of factoring 
large numbers into primes. Using the spin states of electrons in semiconductor 
nanostructures for the quantum-mechanical bits (qubits) of such a computer was 
first proposed in 1998, and has been the subject of much theoretical and 
experimental research ever since.</p>
<p>For electrons to be used in qubits and spintronic devices, their spin states 
must be controllable. Undesired interaction with environmental factors such as 
nuclear spins and phonons can lead to decoherence. To prevent unwanted changes 
to qubits, this decoherence must be small enough that it does not irreversibly 
affect the quantum state of the computer on the time scale of the computing 
operations. In order for spin-based nanosystems to function, they must therefore 
be implemented in materials which have relatively long spin lifetimes. Finding 
out which materials have the best spin dynamic properties will help characterize 
materials for spintronic and quantum computing applications. <b>We propose to 
study the spin dynamics of several spintronic and quantum computing-related 
nanostructure systems, using combined optical and microwave techniques.</b></p>
<h2>Spin Lifetimes</h2>
<p>The spin dynamics of a system are often described using one of three common 
lifetimes, labeled <i>T<sub>1</sub></i>, <i>T<sub>2</sub></i>, and <i>T<sub>2</sub><sup>*</sup></i>. <i>T<sub>1</sub> </i>is the 
&quot;longitudinal&quot; or &quot;spin flip&quot; time, describing how fast spins flip their 
direction parallel or anti-parallel to an applied magnetic field. The energy 
level splitting is: &#916;<i>E </i>= <i>g</i>&#956;<i><sub>B</sub>B</i>, where &#956;<i><sub>B</sub>
</i>is the Bohr magneton and <i>g </i>is the electron g-factor for the 
particular material. Changing the direction of a spin in the presence of a 
magnetic field requires addition or subtraction of some energy; consequently <i>
T<sub>1</sub> </i>is typically the longest of the three times for a given material.</p>
<i>
<p>T<sub>2</sub> </i>is the &quot;spin coherence&quot; or &quot;spin dephasing&quot; time, which describes the 
coherence of an ensemble of spins. The decoherence described by <i>T<sub>2</sub> </i>sets 
the time scale for potential quantum computing&mdash;many calculations will need to be 
done within the <i>T<sub>2</sub> </i>time of the quantum computing material. <i>T<sub>2</sub> </i>is 
also called the <i>transverse </i>relaxation time and is typically the most 
difficult of the three times to quantify. Because there is no energy difference 
in the transverse direction, <i>T<sub>2</sub> </i>is typically shorter than <i>T<sub>1</sub></i>.</p>
<i>
<p>T<sub>2</sub><sup>*</sup> </i>is the &quot;inhomogeneous dephasing&quot; time. It describes the apparent 
dephasing produced by inhomogeneous effects in the material. If the spins behave 
slightly differently in different places in a material (e.g. have slightly 
different g-factors), they get out of phase with each other. This is independent 
of, and cumulative with, the effects which produce <i>T<sub>2</sub></i>-type decoherence; 
thus this is the shortest spin lifetime. Inhomogeneous effects can be 
compensated for through special techniques such as the spin echo, but if no such 
techniques are employed, <i>T<sub>2</sub><sup>*</sup> </i>will be measured instead of <i>T<sub>2</sub></i>. <i>
T<sub>2</sub><sup>*</sup> </i>provides a lower bound for <i>T<sub>2</sub></i>.</p>
<p>For more information, please see this <a href="http://www.ostc.uiowa.edu/~frg/research/spincohere.html">
discussion of semiconductor spin lifetimes and decoherence</a> by Michael Flatt&eacute; at the
University of Iowa.</p>
<h2>Optical Study of Spin</h2>
<p>In most III-V semiconductors, the spin states are connected to optical 
transitions via selection rules. This allows one to study the spins in these 
materials via optics: optically exciting the spins into desired states and/or 
detecting the state of the spins by measuring optical properties. In the 
important material GaAs, for example, spin coherence properties have been 
studied through the following optical methods (not an exhaustive list):</p>
<p>(a) The Hanle effect. This is a method of finding the <i>T<sub>2</sub><sup>*</sup> </i>spin 
lifetime by measuring the depolarization of luminescence in a transverse 
magnetic field. In this technique the spins are oriented using circularly 
polarized light and their states are monitored via the polarization of the 
emitted light. As the field is increased from zero, the spins precess away from 
their initial direction, which causes the emitted light's polarization to 
change. The <i>T<sub>2</sub><sup>*</sup> </i>lifetime is deduced from the width of the depolarization 
vs. field curve.</p>
<p>(b) Time-resolved Faraday or Kerr rotation. This is also a measurement of 
spin precession, typically done at higher fields, which like the Hanle effect 
can yield <i>T<sub>2</sub><sup>*</sup> </i>spin lifetimes. In this technique, the sample is typically 
excited with a short pump beam of circularly polarized light, after which the 
spin states are monitored via the Faraday or Kerr effects acting on a linearly 
polarized probe beam. The Faraday (for transmitted light) and Kerr (for 
reflected light) effects cause the probe beam's angle of polarization to change 
in response to the overall spin polarization of the electrons in the sample. The 
precession of spins due to a transverse external field can be seen directly as 
oscillations in the Faraday rotation as one varies the time delay between pump 
and probe beams.</p>
<p>(c) Time-resolved decay of polarization. This is a measurement of spin decay 
that yields <i>T<sub>1</sub></i>. In this technique, spins are first injected <i>parallel
</i>to an external magnetic field through an optical pump pulse. The state of 
the spins some time later is measured by an optical probe pulse. The change in 
spin states between the two pulses is due to spin flip events, and the spin 
polarization decays according to the spin flip time.</p>
<p>When applied to <i>n</i>-type bulk GaAs samples, these optical techniques 
have resulted in experimental measurements ranging from 5-200 ns for <i>T<sub>2</sub><sup>*</sup></i>, 
and 0.04-20 &#956;s for <i>T<sub>1</sub></i>,&nbsp; depending on details such as sample doping 
level, temperature, and magnitude of external magnetic field. Overall these 
values agree fairly well with spin properties measured through other methods and 
those predicted theoretically.</p>
<h2>Spin Resonance</h2>
<p>Optical methods are not the only options for studying spin. Microwave 
techniques in particular have been used for years to study the spin of 
electrons. The microwave studies of spin center around the principle of magnetic 
resonance: transitions between the two spin-split energy levels of the electrons 
can be induced when microwaves with the corresponding energy are applied. The 
resonant microwave frequency <i>f </i>is given by the condition: <i>hf </i>= &#916;<i>E
</i>= <i>g</i>&#956;<i><sub>B</sub>B. </i>When the spins are manipulated in a standard magnetic 
resonance experiment (also called &quot;electron spin resonance,&quot; ESR), the widths of 
the resonance peaks depend on the spin lifetime and can be an additional method 
for obtaining <i>T<sub>2</sub><sup>*</sup></i>.</p>
<p>More important, however, is the potential for coherently rotating the spins 
in a controlled fashion via<i> pulsed </i>microwaves. As an intense resonant 
microwave field is applied, the electrons are driven back and forth between spin 
states; these are Rabi oscillations. By interrupting the oscillation cycle and 
applying pulses of varying strengths at various time delays, the electrons'spins can be rotated to arbitrary directions. In these resonance experiments, <b>
the microwaves are directly and controllably manipulating the electrons'spin</b>. 
In the quantum computing context, these would be called <i>one qubit operations</i>, 
which are needed in all quantum computing implementations. Through clever pulse 
sequences, additional spin dynamic information can be obtained; the Hahn spin 
echo is one such sequence and is the usual method for determining the spin 
coherence time <i>T<sub>2</sub></i>.</p>
<h2>Combined Optical/Microwave Techniques</h2>
<p>Traditional microwave experiments detect the absorbed microwave power; these 
experiments are not feasible in nanostructures because there are not enough 
spins in the material to produce a measureable effect. However, microwave 
resonance can be combined with optical detection to dramatically increase the 
sensitivity of spin resonance experiments. This technique, called &quot;optically 
detected magnetic resonance&quot; (ODMR) has been successfully applied to 
semiconductors for many years. Under the proper conditions, optical detection 
schemes can even allow one to detect the state of individual spins, as has been 
done with the NV-center defect in diamond.</p>
<h2>Summary</h2>
<p>The study of spin dynamics in semiconductors is extremely important given the 
potentially transformative effects from spintronics and quantum computing. 
Optical methods have proven successful at studying electron spins in GaAs and 
related materials; microwave resonance techniques also have a long history of 
success. We propose to combine both in order to study the spin dynamics of 
several important semiconductor nanostructure systems.</p>


